CREATE TABLE IF NOT EXISTS persona (
  persona_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  username VARCHAR(255) UNIQUE NOT NULL CHECK (LENGTH(username) > 0),
  social_net_id SMALLINT NOT NULL,
  last_scanned_at INTEGER DEFAULT 0,
  first_scanned_at INTEGER DEFAULT 0,
  times_scanned INTEGER DEFAULT 0,
  latest_activity_at INTEGER DEFAULT 0,
  priority_score INTEGER NOT NULL DEFAULT 1,
  total_content_count INTEGER NOT NULL DEFAULT 0,
  average_spam NUMERIC(4,2) NOT NULL DEFAULT 0.0,
  average_emotions JSONB NOT NULL DEFAULT '{}'::jsonb,
  average_personalities JSONB NOT NULL DEFAULT '{}'::jsonb,
  thirdparty JSONB NOT NULL DEFAULT '{}'::jsonb
);
CREATE INDEX IF NOT EXISTS persona_username_idx ON persona USING btree(username);

CREATE TABLE IF NOT EXISTS yearly_activity (
  yearly_activity_id SERIAL PRIMARY KEY,
  persona_id uuid NOT NULL,
  yearly_activity INTEGER[366][24] NOT NULL,
  CONSTRAINT fk_persona_id
    FOREIGN KEY(persona_id)
      REFERENCES persona(persona_id)
      ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS yearly_activity_persona_id_idx ON yearly_activity USING btree(persona_id);

CREATE TABLE IF NOT EXISTS weekly_activity (
  weekly_activity_id SERIAL PRIMARY KEY,
  persona_id uuid NOT NULL,
  sun_to_mon_probs NUMERIC(6,4)[7] NOT NULL DEFAULT ARRAY[0,0,0,0,0,0,0],
  sun_to_mon_counts INTEGER[7] NOT NULL DEFAULT ARRAY[0,0,0,0,0,0,0], -- if user was active on particular weekday +1; does not matter if the activity contains 1 or 1+ content
  CONSTRAINT fk_persona_id
    FOREIGN KEY(persona_id)
      REFERENCES persona(persona_id)
      ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS weekly_activity_persona_id_idx ON weekly_activity USING btree(persona_id);

CREATE TABLE IF NOT EXISTS content (
  content_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  content_type_id SMALLINT NOT NULL,
  persona_id uuid NOT NULL,
  spam NUMERIC(4,2) NOT NULL DEFAULT 0.0,
  emotions JSONB NOT NULL DEFAULT '{}'::jsonb,
  personalities JSONB NOT NULL DEFAULT '{}'::jsonb,
  --vdb_text_pointer uuid NOT NULL, -- points to vector db's uuid
  thirdparty_url VARCHAR(512) NOT NULL, -- points to reddit's content url
  thirdparty_created_at INTEGER DEFAULT 0, -- points to reddit's content created_utc
  thirdparty JSONB NOT NULL DEFAULT '{}'::jsonb,
  CONSTRAINT fk_persona_id
    FOREIGN KEY(persona_id)
      REFERENCES persona(persona_id)
      ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS content_type (
  content_type_id SMALLSERIAL PRIMARY KEY,
  name VARCHAR(31)
);
